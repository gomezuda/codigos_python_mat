valor = int(input("Informe um valor queride:"))

if 0 <= valor <= 25:
    print("O número está na faixa de 0 à 25.")
elif 26 <= valor <= 50:
    print("O número está na faixa de 26 à 50.")
elif 51 <= valor <= 75:
    print("O número está na faixa de 51 à 75.")
elif 76 <= valor <= 100:
    print("O número está na faixa de 76 à 100.")